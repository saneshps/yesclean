    <script type="text/javascript">
        $(document).ready(function(e) {


            var loading = false;
            $('#frmregisteroh').on('submit', function(e) {
                e.preventDefault();
                loading = true;
            });

        });

        // SERVICE REQUESTS
        if ($("#frmregisteroh").length > 0) {
            $("#frmregisteroh").validate({
                rules: {
                    firstname: {
                        required: true,
                        maxlength: 50
                    },
                    email: {
                        required: true,
                        maxlength: 50,
                        email: true,
                    },
                    company: {
                        required: true,
                        maxlength: 50
                    },
                    participants: {
                        required: true
                    }
                },
                messages: {
                    firstname: {
                        required: "Please enter name",
                        maxlength: "Your name maxlength should be 50 characters long."
                    },
                    email: {
                        required: "Please enter valid email",
                        email: "Please enter valid email",
                        maxlength: "The email name should less than or equal to 50 characters",
                    },
                    company: {
                        required: "Please enter company",
                        maxlength: "Your name maxlength should be 50 characters long."
                    },
                    participants: {
                        required: "Please enter no of participants"
                    }
                },
                submitHandler: function(form) {

                    $('#submit').html('Please Wait...');
                    $("#submit").attr("disabled", true);
                    $.ajax({
                        url: "./submitopenhouse.php",
                        type: "POST",
                        data: $('#frmregisteroh').serialize(),
                        success: function(response) {

                            $('#submit').html('Submit');
                            $("#submit").attr("disabled", false);
                            //$('#submit-msg').html(response.message).css('display', 'block').delay(5000).fadeOut('slow');

                            document.getElementById("frmregisteroh").reset();
                            alert("You are registered successfully !!");
                        }
                    });
                }
            })
        }
    </script>


