		<li>Time-consuming</li>
		<p>We tend to apply more foam in areas where there is a lot of debris, oil, or grease. Definitely, the site will get cleaned but the problem is, it will take a lot of time to wash it off.</p>

		<li>Not a good substitute for dishwasher cleaner</li>
		<p>Since foam creates air pockets, this will make you lose the pressure in cleaning, when it is used in a dishwasher. In this process, the foam will create bubbles around the pump impeller and prevent the water from hitting the dishes. And the dishes won't get cleaned properly.</p>

