          <h1>The Technology Behind Sweeper Robots</h1>
          <img src="../img/blog/technology-behind-sweeper-robots.jpg" style="width:100%;">
          <p>&nbsp;</p>

          <p>It is said that the first-ever robotic vacuum cleaner “Trilobite” introduced in the year 1996, was made by a Swedish company, Electrolux. Robotic vacuum cleaners are also called “Robovacs” or “Roombas”. They are autonomous vacuums that are currently considered the most popular consumer robots. Their compact size is perfect to navigate around the nook and corner of any space whether it's in a home or office space.</p>

          <h2>The Technology Behind Sweeper Robots</h2>

          <p>Sweeper robots are capable to go under the furniture and around the clutters and when completed returning to their charging dock.These <strong><a href="https://www.yesclean.ae/sweeper-robot.php">sweeper vacuum robot’s</a></strong> controllers and cleaning routines are programable and are combined with sensors and robotic drives. They can clean dirt, dust, pet hair, allergens, and more. Some of the technologies that are used by sweeping robots are;</p>

          <h2>The Sensor</h2>

          <p>This is the main feature of robot vacuums. The introduction of the sensor helped them to navigate through the floor. They abled the cleaner to see what was in front of their way, whether it was a cliff, any objects, stairs, etc. Before the invention of sensors in vacuums, the automated vacuum used to bump their way around the room, and not only this they tend to miss the key areas. Compared to robotic sweepers, their running time is also short. This problem is solved by the innovation of sensors and motor controllers in combination with dedicated open-source software and drivers. Sensors are of different kinds which have improved navigation and cleaning, let's look into that.</p>

          <h2>Ultrasonic Time-Of-Flight</h2>

          <p>Ultrasonic ToF sensors are independent of sensing by target color and optical transparency. Instead, they work in any lighting conditions and are capable to provide millimeter-accurate range measurements. They have a wide field of view that enables them to measure simultaneous ranges of multiple objects. If there is a pet or toy lying in its way then they deviate its route to avoid a collision.</p>

          <h2>Short-Range Ultrasonic ToF</h2>

          <p>Compared to Ultrasonic time-of-flight Short-range ultrasonic ToFs are a little more advanced. Since they can identify different floor types. To identify if the target surface is hard or soft, it is working on an average amplitude of a reflected ultrasonic signal. While cleaning a house when they are navigating from tile to carpet or carpet to hardwood floor, these machines know how much effort it has to put into different kinds of flooring whether it’s tile, carpeted, or wooden. They can also detect the cliff when it's on the stairs or balcony to avoid falls.</p>

          <h2>VSLAM And LiDAR</h2>

          <p>visual simultaneous localization and mapping (VSLAM) or Light Detection and Ranging (LiDAR) technology is something most of the company depending on developing high-end robot vacuums because they will help to build a virtual map of the room. Since they are viewing a virtual map of the room that they are going to clean they are capable to clean more efficiently covering the entire home. But the only drawback it has got is that, if you lift the machine and keep it down then it fails to know the new location. Again to locate they have to go off in a random direction and have to detect and trace the wall to find out where it has to start in relevant to the map.</p>

          <h2>Inertial Measurement Units (IMU)</h2>

          <p>Both from a linear and rotational perspective, IMUs take the roll, pitch, and yaw of the robot sweeper. They know every step and movement they are taking while cleaning. There can be errors in the location where it is supposed to be and where it is now, but the interesting part is that IMU can hold that position in a very accurate way. These Sweepers won't clean the same location again and again, even if they run out of battery they are capable to clean from where they stopped after charging. </p>

          <p>Because they have the accurate mapping of the room in addition to a rotational and linear movement which enables them to detect where it was and where it is going. Whatever situation you give whether you pick up the Robovac and keep it somewhere else or you keep it in a different direction, they will start from where they stopped. This makes the vacuum cleaner next level compared to VSLAM and LiDAR features. How do they work? It is done by combining the inertial measurements and measurements from the wheel’s rotations from the IMU and object detection done by the ToF sensors.</p>

          <h2>Smart Speaker Microphones</h2>

          <p>Another feature of a Robotic sweeper is having voice assistants. As microphones become an essential sensor technology after the implementation of artificial intelligence (AI). With the advancement of technology motors and brushes become quieter compared to the normal old-style vacuum cleaners and microphone technology has progressed. Now users can give commands to the robotic vacuum sweepers and they are also capable to disregard other certain noises.</p>

          <p>When we give a specific command to it it will stop working to receive that command and will do as per the command. If you tell them to a specific area to clean like sudden spills or something they will come and clean that area. Interesting, Right?</p>

          <h2>Embedded Motor Controllers</h2>

          <p>Embedded motor controllers ensure a high level of accuracy in the direction by turning the gears to navigate in the correct direction robot vacuum when the wheel is actually turned 90 degrees as opposed to 88 degrees. Without embedded motor controllers the Robo sweeper will be way off track after a certain amount of time. They can be flexible with or without sensors which will make robot vacuum scalable.</p>

          <h2>Pressure sensors</h2>

          <p>Pressure sensors are used to monitor the flow of air through the dustbin so that we can estimate the level of dust inside the dust box. Due to an increase in suction dust or clogging of the filter, the air pressure inside the dustbin starts to decrease when the airflow begins to stagnate compared to the air pressure when the dustbin is empty. But to measure the outside air pressure, it is better to detect it as a differential pressure that uses a similar pressure sensor.Most premium vacuum robots have the capability to suck out the contents automatically from the dust box and return to the base to empty its content. After emptying they will return back to continue the cleaning work from where it stopped.</p>

          <h2>Auto-recharging</h2>

          <p>Only an accurate current and voltage measurement can define the battery’s state of charge (SoC). When the battery is low or when it reaches an arbitrary SoC level, the robot sweeper stops cleaning by itself and returns to the base to recharge. And they are also capable to go back to their last known place where they stopped cleaning and continuing the cleaning. Whether your room is big or small, or your house is big or small, if you have multiple charging points and points to empty the dustbin then they can cover the entire floor space.</p>

          <h2>Thermistors</h2>

          <p>Thermistors are temperature sensors. The temperatures of the motors and brush gears and the running temperature of the MCU or MPU are all measured by using Thermistors. There are situations where the running way will get too hot, and at that time the vacuum will get the command to take a break. When this happens sometimes the robotic sweeper will run a few system diagnostics to find out what is causing the problem.</p>

          <p>There are chances that some things like elastic bands or excess hair get stuck which makes the motors overcompensate and overheat. If we leave it unnoticed there are chances for motor degradation. This can be avoided if we could use a microphone to detect whether the motors are running abnormally. It is the robot vacuum cleaner developer's responsibility to understand how a motor can sound at a certain threshold of frequency</p>

          <h3>Conclusion </h3>

          <p>A sweeper vacuum robot saves energy and precious time, the cleaning schedule can be programmed. The sweeper robot has low maintenance which can result in big savings, they are user friendly, they have an integrated dust bin, and works silently. It can be costly but it is worth investing in.</p>

