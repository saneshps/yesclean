<h3>How does it work? </h3>
<p>
    A scrubber dryer cleans floors and surfaces via mechanically operated, rotating floor pads or brushes. Floor pads come in varying abrasives to suit all cleaning tasks. Cleaning is done by using a mixture of water and a detergent solution. The machine will release the solution into the rotating brushes/pads, when in forwarding operation. The motion of the brushes along with the detergent lifts and removes dirt from floors. Then the machine collects the leftover detergent and debris. This is then stored in the recovery tank which can be emptied at the end of the cleaning process.
</p>

