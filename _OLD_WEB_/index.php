      var scrollTop = $(window).scrollTop();
      if (scrollTop > windowHeight) return;
      var inner = slideshow.find('.slideshow-inner');
      var newHeight = windowHeight - (scrollTop / 2);
      var newTop = scrollTop * 0.8;

      inner.css({
        transform: 'translateY(' + newTop + 'px)',
        height: newHeight
      });
    }

